package com.init.address;

/**
 *
 * @author amnesia
 */
public class Address {

    protected int ID;
    protected int IDProvinsi;
    protected int IDKabupaten;
    protected int IDKecamatan;
    protected int IDKeluarahan;
    protected String NamaProvinsi;
    protected String NamaKabupaten;
    protected String NamaKecamatan;
    protected String NamaKelurahan;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getIDProvinsi() {
        return IDProvinsi;
    }

    public void setIDProvinsi(int IDProvinsi) {
        this.IDProvinsi = IDProvinsi;
    }

    public int getIDKabupaten() {
        return IDKabupaten;
    }

    public void setIDKabupaten(int IDKabupaten) {
        this.IDKabupaten = IDKabupaten;
    }

    public int getIDKecamatan() {
        return IDKecamatan;
    }

    public void setIDKecamatan(int IDKecamatan) {
        this.IDKecamatan = IDKecamatan;
    }

    public int getIDKeluarahan() {
        return IDKeluarahan;
    }

    public void setIDKeluarahan(int IDKeluarahan) {
        this.IDKeluarahan = IDKeluarahan;
    }

    public String getNamaProvinsi() {
        return NamaProvinsi;
    }

    public void setNamaProvinsi(String NamaProvinsi) {
        this.NamaProvinsi = NamaProvinsi;
    }

    public String getNamaKabupaten() {
        return NamaKabupaten;
    }

    public void setNamaKabupaten(String NamaKabupaten) {
        this.NamaKabupaten = NamaKabupaten;
    }

    public String getNamaKecamatan() {
        return NamaKecamatan;
    }

    public void setNamaKecamatan(String NamaKecamatan) {
        this.NamaKecamatan = NamaKecamatan;
    }

    public String getNamaKelurahan() {
        return NamaKelurahan;
    }

    public void setNamaKelurahan(String NamaKelurahan) {
        this.NamaKelurahan = NamaKelurahan;
    }

}
