package com.init.jenistugas;

/**
 *
 * @author amnesia
 */
public class JenisTugas {

    private int id;
    private String kode;
    private String jenistugas;
    private int skor;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getJenistugas() {
        return jenistugas;
    }

    public void setJenistugas(String jenistugas) {
        this.jenistugas = jenistugas;
    }

    public int getSkor() {
        return skor;
    }

    public void setSkor(int skor) {
        this.skor = skor;
    }

}
