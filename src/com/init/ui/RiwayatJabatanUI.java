    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = tabel_riwayat.getSelectedRow();
        if (row >= 0) {
            int kode = Integer.valueOf(tabel_riwayat.getValueAt(row, 0).toString());
            int opsi = JOptionPane.showConfirmDialog(null, "Yakin akan menghapus data ?", "Perhatian!", JOptionPane.OK_CANCEL_OPTION);
            if (opsi == JOptionPane.OK_OPTION) {
                DaoFactory.getRiwayatJabatanDao().deleteRiwayatJabatan(kode);
                loadtable();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = tabel_riwayat.getSelectedRow();
        if (row >= 0) {
            int kode = Integer.valueOf(tabel_riwayat.getValueAt(row, 0).toString());
            int opsi = JOptionPane.showConfirmDialog(null, "Yakin akan merubah data ?", "Perhatian!", JOptionPane.OK_CANCEL_OPTION);
            if (opsi == JOptionPane.OK_OPTION) {
                RiwayatJabatan riwayatJabatan = DaoFactory.getRiwayatJabatanDao().getRiwayatJabatanByID(kode);
                Session.setRiwayatJabatan(riwayatJabatan);
                System.out.println("Session ada " + Session.getRiwayatJabatan().getId());
                UpdateRiwayatJabatanUI jabatanUI = new UpdateRiwayatJabatanUI();
                jabatanUI.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

