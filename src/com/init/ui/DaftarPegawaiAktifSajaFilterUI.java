    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:       
        Cabang cabang = DaoFactory.getCabangDao().getCabangByNamaCabang(combocabang.getSelectedItem().toString());
        Unit unit = DaoFactory.getUnitDao().getUnitByNama(combounit.getSelectedItem().toString());        
        HashMap params = new HashMap();
        params.put("cabang", String.valueOf(cabang.getIdCabang()));
        params.put("unit", String.valueOf(unit.getID()));
        new PrintReport("./report/daftar_pegawai_komplit_filter.jasper", params);
    }//GEN-LAST:event_jButton2ActionPerformed

