    private void initAPP(){
        combocabang.removeAllItems();
        combounit.removeAllItems();
        List<Cabang> cabangs = DaoFactory.getCabangDao().getAllCabang();
        List<Unit> units = DaoFactory.getUnitDao().getAllUnit();   
        for (int i = 0; i < cabangs.size(); i++) {
            combocabang.addItem(cabangs.get(i).getNamacabang());
        }
        for (int i = 0; i < units.size(); i++) {
            combounit.addItem(units.get(i).getNamaUnit());
        }
    }

    private void loadtable() {
        PegawaiTabelModelDetil detil = new PegawaiTabelModelDetil(DaoFactory.getPegawaiDao().getAllPegawai());
        rowSorter = new TableRowSorter(detil);
        tabelpegawai.setRowSorter(rowSorter);
        tabelpegawai.setModel(detil);
        tabelpegawai.getColumnModel().getColumn(0).setPreferredWidth(20);
        tabelpegawai.getColumnModel().getColumn(1).setPreferredWidth(170);
        tabelpegawai.getColumnModel().getColumn(2).setPreferredWidth(50);
        tabelpegawai.getColumnModel().getColumn(4).setPreferredWidth(170);
    }

