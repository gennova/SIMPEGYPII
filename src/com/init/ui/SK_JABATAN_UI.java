    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (Session.getNUK().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Klik Simpan Data Pegawai Terlebih Dahulu");
        } else {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tmt = sdf.format(tmtDate.getDate());
            String tmtIndo = DaoFactory.FormatTanggalIndonesia(tmt);
            String nomorsk = txtNomorSK.getText();
            String tanggal = sdf.format(tanggalSKDate.getDate());
            String tanggalIndo = DaoFactory.FormatTanggalIndonesia(tanggal);
            PekerjaanJabatan pekerjaanJabatan = Session.getPekerjaanJabatan();
            PekerjaanJabatan pekerjaanJabatanNew = DaoFactory.getPekerjaanJabatanDao().getPekerjaanJabatanByNUK(Session.getNUK());
            SK_jabatan sk = new SK_jabatan();
            sk.setNuk(Session.getNUK());
            sk.setId_jabatan(pekerjaanJabatanNew.getJabatan().getId());
            sk.setId_pekerjaan_jabatan(pekerjaanJabatanNew.getId());
            sk.setPegawai(DaoFactory.getPegawaiDao().getPegawaiByNUK(Session.getNUK()));
            sk.setTmt(tmt);
            sk.setTmt_indo(tmtIndo);
            sk.setNo_sk(nomorsk);
            sk.setTanggal(tanggal);
            sk.setTanggal_indo(tanggalIndo);
            DaoFactory.getsK_jabatan_dao().InsertSK_Jabatan(DaoFactory.getPegawaiDao().getPegawaiByNUK(Session.getNUK()), sk);
            JOptionPane.showMessageDialog(null, "Data SK Berhasil Disimpan");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

