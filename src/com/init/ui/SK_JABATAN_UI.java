    private void initapp() {
        SK_jabatan sK = DaoFactory.getsK_jabatan_dao().getSKJabatanByNUK(Session.getNUK());        
        if (sK.getNuk() == null) {
            tmtDate.setDate(GregorianCalendar.getInstance().getTime());
            txtNomorSK.setText("");
            tanggalSKDate.setDate(GregorianCalendar.getInstance().getTime());
        } else {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            SK_jabatan sK_jabatan = DaoFactory.getsK_jabatan_dao().getSKJabatanByNUK(Session.getNUK());
            try {
                tmtDate.setDate(sdf.parse(sK_jabatan.getTmt()));
                txtNomorSK.setText(sK_jabatan.getNo_sk());
                tanggalSKDate.setDate(sdf.parse(sK_jabatan.getTanggal()));
            } catch (ParseException ex) {
                Logger.getLogger(SK_JABATAN_UI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

