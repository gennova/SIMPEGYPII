package com.init.provinsi;

/**
 *
 * @author amnesia
 */
public class Provinsi {

    private int id;
    private String kode;
    private String namaprovinsi;

    public void setID(int id) {
        this.id = id;
    }

    public int getID() {
        return id;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getKode() {
        return kode;
    }

    public void setNamaProvinsi(String nama) {
        this.namaprovinsi = nama;
    }

    public String getNamaProvinsi() {
        return namaprovinsi;
    }

}
